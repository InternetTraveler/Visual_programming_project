namespace VisualProgramming.Domain.Base;

/// <summary>
/// Представляет абстрактную базовую сущность 
/// в доменной модели с идентификатором указанного типа.
/// </summary>
/// <typeparam name="TId">Тип идентификатора 
/// сущности. Должен быть значимым типом и 
/// реализовывать IEquatable&lt;TId&gt;.</typeparam>
/// <remarks>
/// Сущности в доменной модели идентифицируются 
/// по их идентификатору, а не по атрибутам.
/// Два объекта Entity с одинаковым Id считаются 
/// одинаковыми сущностями.
/// </remarks>
public abstract class Entity<TId>(TId id) where TId : struct, IEquatable<TId>
{
    /// <summary>
    /// Получает уникальный идентификатор сущности.
    /// </summary>
    /// <value>Идентификатор типа TId.</value>
    /// <remarks>
    /// Идентификатор устанавливается при создании сущности 
    /// и не может быть изменен в течение всего жизненного цикла.
    /// </remarks>
    public TId Id { get; } = id;

    /// <summary>
    /// Инициализирует новый экземпляр класса Entity 
    /// без явного указания идентификатора.
    /// </summary>
    /// <remarks>
    /// Этот конструктор используется для десериализации или рефлексии.
    /// Устанавливает Id в значение по умолчанию для типа TId.
    /// </remarks>
    protected Entity() : this(default!) { }
}